      var coupon = $("#coupon").val();
      console.log(coupon);
      jQuery.ajax({
        type: "POST",
        url: "validate_coupon.php",
        data: {
          "coupon": coupon
        },
        success: function(response) {
          // console.log(typeof response);
          response = JSON.parse(response)
          if(response.success) {
            let actual_price = $("#final-price").text();
            final_price = actual_price - (actual_price * response.discount / 100);
            $("#actual-price-td").text(actual_price);
            $("#coupon-discount-td").text(response.discount);
            $("#discounted-price-td").text(final_price);

            $('input[name="amount"]').val(final_price);
          }
          let color = response.success ? "green" : "red";
          let text = response.success ? "הוחל קופון!" : "קופון אינו קיים";
          $("#coupon_message").html(`<span style="color: ${color}">${text}</span>`);
        }
      });
